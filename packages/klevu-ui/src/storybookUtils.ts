export const KlevuProductElement = (product: KlevuRecord, args?: object) => {
  const element = document.createElement("klevu-product")
  element.product = product
  if (args) {
    for (const [key, value] of Object.entries(args)) {
      // @ts-expect-error
      element[key] = value
    }
  }
  return element
}

export function autofillMeta(tag: string, meta: Meta = {}): Meta {
  const data: JsonDocs = jsdocs as any
  const comp = data.components.find((c) => c.tag == tag)
  if (!comp) {
    return {}
  }

  const argTypes: ArgTypes = {}
  for (const a of comp.props) {
    if (!a.name) {
      continue
    }

    const noUndefinedValues = a.values.filter((v) => v.type !== "undefined")

    const isOptions = Boolean(noUndefinedValues[0].value) && noUndefinedValues[0].type === "string"

    argTypes[a.name] = {
      name: Boolean(a.attr) ? a.attr : a.name,
      description: a.docs,
      table: {
        category: Boolean(a.attr) ? "Attributes" : "Properties",
        type: {
          summary: a.type,
        },
        defaultValue: {
          summary: a.default,
        },
      },
      required: a.required,
      type: noUndefinedValues.length === 1 ? noUndefinedValues[0].type : ("string" as any),
    }

    if (!a.attr) {
      argTypes[a.name].type = undefined
      argTypes[a.name].control = "object"
    }

    if (isOptions) {
      argTypes[a.name].options = noUndefinedValues.map((o) => o.value)
      argTypes[a.name].control = "radio"
    }
  }
  const handles: string[] = []
  for (const e of comp.events) {
    if (!e.event) {
      continue
    }
    handles.push(e.event)
    argTypes[e.event] = {
      action: e.docs,
      description: e.docs,
      table: {
        category: "Events",
      },
    }
  }
  for (const s of comp.slots) {
    if (!s.name) {
      continue
    }
    argTypes[s.name] = {
      description: s.docs,
      table: {
        category: "Slots",
      },
    }
  }
  for (const m of comp.methods) {
    if (!m.name) {
      continue
    }
    argTypes[m.name] = {
      description: m.docs,
      table: {
        category: "Methods",
        type: {
          summary: m.signature,
        },
      },
    }
  }

  const cssprops = comp.docsTags.filter((d) => d.name === "cssprop")
  for (const prop of cssprops) {
    if (!prop.text) {
      continue
    }
    const [variable, defaultVal, ...desc] = prop.text.split(" ")

    if (!variable) {
      continue
    }

    argTypes[variable] = {
      description: desc.join(" "),
      table: {
        category: "CSS Properties",
        defaultValue: {
          summary: defaultVal,
        },
      },
    }
  }

  const cssparts = comp.docsTags.filter((d) => d.name === "csspart")
  for (const part of cssparts) {
    if (!part.text) {
      continue
    }
    const [variable, defaultVal, ...desc] = part.text.split(" ")
    argTypes[variable] = {
      description: desc.join(" "),
      table: {
        category: "CSS Parts",
        defaultValue: {
          summary: defaultVal,
        },
      },
    }
  }

  const sortKeys = ["Attributes", "Properties", "Events", "Slots", "Methods", "CSS Properties", "CSS Parts"]

  const sortedArgTypeKeys = Object.keys(argTypes).sort((a, b) => {
    // sort keys by sortKeys variable
    const aIndex = sortKeys.indexOf(argTypes[a].table.category)
    const bIndex = sortKeys.indexOf(argTypes[b].table.category)
    if (aIndex === bIndex) {
      return a.localeCompare(b)
    }
    return aIndex - bIndex
  })

  const sortedArgTypes = sortedArgTypeKeys.reduce((acc, key) => {
    acc[key] = argTypes[key]
    return acc
  }, {} as ArgTypes)

  return merge(meta, {
    component: tag,
    parameters: {
      actions: {
        handles,
      },
    },
    argTypes: sortedArgTypes,
  })
}

export function MDXAutoFillMeta(tag: string, meta: Meta = {}, settings?: KlevuUIGlobalSettings) {
  const { argTypes, parameters } = autofillMeta(tag, meta)

  const data: JsonDocs = jsdocs as any
  const comp = data.components.find((c) => c.tag == tag)

  const klevuInitInject = (story: any) => {
    const key = localStorage.getItem("klevu-api-key")
    const url = localStorage.getItem("klevu-url")

    if (key && url) {
      return html` <klevu-init api-key=${key} url=${url}> ${story()} </klevu-init> `
    }

    return html`
      <klevu-init
        .settings=${settings}
        api-key="klevu-164651914788114877"
        url="https://eucs29v2.ksearchnet.com/cs/v2/search"
      >
        ${story()}
      </klevu-init>
    `
  }

  return {
    argTypes,
    parameters,
    description: comp?.docs,
    decorators: [withActions as any, klevuInitInject],
  }
}

