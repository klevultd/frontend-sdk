import { KlevuApiRawResponse, KlevuRecord } from "@klevu/core"
import { JsonDocs } from "@stencil/core/internal"
import { ArgTypes, Meta } from "@storybook/web-components"
// @ts-inore
import jsdocs from "../dist/docs/klevu-ui-docs.json"
import merge from "lodash.merge"

export const KlevuProductElement = (product: KlevuRecord, args?: object) => {
  const element = document.createElement("klevu-product")
  element.product = product
  if (args) {
    for (const [key, value] of Object.entries(args)) {
      // @ts-expect-error
      element[key] = value
    }
  }
  return element
}

export function autofillMeta(tag: string, meta: Meta): Meta {
  const data: JsonDocs = jsdocs as any
  const comp = data.components.find((c) => c.tag == tag)
  if (!comp) {
    return {}
  }

  const argTypes: ArgTypes = {}
  for (const a of comp.props) {
    if (!a.name) {
      continue
    }

    const noUndefinedValues = a.values.filter((v) => v.type !== "undefined")

    const isOptions = Boolean(noUndefinedValues[0].value) && noUndefinedValues[0].type === "string"

    argTypes[a.name] = {
      name: a.name,
      description: a.docs,
      table: {
        category: "Attributes",
        type: {
          summary: a.type,
        },
        defaultValue: {
          summary: a.default,
        },
      },
      required: a.required,
      type: noUndefinedValues.length === 1 ? noUndefinedValues[0].type : ("string" as any),
    }

    if (!a.attr) {
      argTypes[a.name].type = undefined
      argTypes[a.name].control = "object"
    }

    if (isOptions) {
      argTypes[a.name].options = noUndefinedValues.map((o) => o.value)
      argTypes[a.name].control = "radio"
    }
  }
  const handles: string[] = []
  for (const e of comp.events) {
    handles.push(e.event)
    argTypes[e.event] = {
      action: e.docs,
      description: e.docs,
      table: {
        category: "Events",
      },
    }
  }
  for (const s of comp.slots) {
    argTypes[s.name] = {
      description: s.docs,
      table: {
        category: "Slots",
      },
    }
  }
  for (const m of comp.methods) {
    argTypes[m.name] = {
      description: m.docs,
      table: {
        category: "Methods",
        type: {
          summary: m.signature,
        },
      },
    }
  }

  const cssprops = comp.docsTags.filter((d) => d.name === "cssprop")
  for (const prop of cssprops) {
    if (!prop.text) {
      continue
    }
    const [variable, defaultVal, ...desc] = prop.text.split(" ")
    argTypes[variable] = {
      description: desc.join(" "),
      table: {
        category: "CSS Properties",
        defaultValue: {
          summary: defaultVal,
        },
      },
    }
  }

  const cssparts = comp.docsTags.filter((d) => d.name === "csspart")
  for (const part of cssparts) {
    if (!part.text) {
      continue
    }
    const [variable, defaultVal, ...desc] = part.text.split(" ")
    argTypes[variable] = {
      description: desc.join(" "),
      table: {
        category: "CSS Parts",
        defaultValue: {
          summary: defaultVal,
        },
      },
    }
  }

  return merge(meta, {
    component: tag,
    parameters: {
      actions: {
        handles,
      },
    },
    argTypes,
  })
}

